import React from 'react';
import { useState } from 'react';
import Footer from '../../components/footer/Footer';
import SocialFooter from '../../components/socialFooter/socialFooter';
import Topbar from '../../components/topbar/Topbar';
import './policy.css';

function Policy() {
  const [selected, setSelected] = useState(0);
  const info = (i) => {
    if (selected === i) {
      return setSelected(null);
    }
    setSelected(i);
  };
  return (
    <>
      <Topbar />

      <div className="policy-container">
        <div className="accordion">
          <h1 className="policy-title">LifeCloud מדיניות פרטיות</h1>

          {data.map((item, i) => (
            <div className="item" key={'item-' + i}>
              <div className="title" onClick={() => info(i)}>
                <h2>{item.title}</h2>
                <span className="span2">{selected === i ? '-' : '+'}</span>
              </div>
              <div className={selected === i ? 'content show' : 'content'}>
                {item.sections &&
                  item.sections.map((section, i) => (
                    <div className="answer-section" key={'section-' + i}>
                      <div className="section-title">
                        {section.subTitle && section.subTitle}
                      </div>
                      <div>
                        {section.paragraphs &&
                          section.paragraphs.map((p, i) => (
                            <div
                              className={
                                'answer-paragraph ' +
                                (section.subTitle ? 'small-text ' : '') +
                                (p.noMargin ? 'no-margin' : '') +
                                (p.bold ? ' bold' : '')
                              }
                              key={'paragraph-' + i}
                            >
                              {p.p}
                            </div>
                          ))}
                      </div>
                    </div>
                  ))}
              </div>
            </div>
          ))}
        </div>
      </div>

      <SocialFooter backgroundColor="#F5FCFF" color="#6097bf" />
      <Footer />
    </>
  );
}

